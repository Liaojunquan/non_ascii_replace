def check_ascii(text):
    if text == '':
        return False
    is_ascii = True
    for c in text:
        if ord(c) > 126:
            is_ascii = False
            break
    return is_ascii


def replace_non_ascii_char(source):
    result = ''
    for c in source:
        if ord(c) > 126:
            result += find_char(c)
        else:
            result += c
    return result.strip()
